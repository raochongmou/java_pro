package com.bjpowernode.day20220513;

public class Customer {
    private int  age;
    private String name;
    private String addr;

    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return  this.age;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return  this.name;
    }
    public void setAddr(String addr) {
        this.addr = addr;
    }
    public String getAddr() {
        return  this.addr;
    }
}
